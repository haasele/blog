#!/usr/bin/env node

/**
 * Mizuki content repository initialization script
 * Helps users quickly set up code/content separation
 */

import { execSync } from "child_process";
import fs from "fs";
import path from "path";
import readline from "readline";
import { fileURLToPath } from "url";
import { loadEnv } from "./load-env.js";

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);
const rootDir = path.resolve(__dirname, "..");

loadEnv();
console.log("Loaded .env configuration\n");

const rl = readline.createInterface({
	input: process.stdin,
	output: process.stdout,
});

function question(query) {
	return new Promise((resolve) => rl.question(query, resolve));
}

function exec(command, options = {}) {
	try {
		return execSync(command, { stdio: "inherit", ...options });
	} catch (error) {
		console.error(`Command failed: ${command}`);
		throw error;
	}
}

async function main() {
	console.log("Mizuki content repository setup\n");

	console.log("Content will be managed in a separate repository.\n");

	// Prompt for content repository URL
	const repoUrl = await question("Enter content repository URL: ");

	if (!repoUrl.trim()) {
		console.error("Error: content repository URL cannot be empty.");
		rl.close();
		return;
	}

	// Confirm settings
	console.log("\nCurrent configuration:");
	console.log("  Mode: separate repository");
	console.log(`  Repository: ${repoUrl.trim()}`);

	const confirm = await question("\nProceed with initialization? (y/n): ");

	if (confirm.toLowerCase() !== "y") {
		console.log("Initialization cancelled.");
		rl.close();
		return;
	}

	console.log("\nStarting initialization...\n");

	// Create .env file
	const envPath = path.join(rootDir, ".env");
	const envContent = `# Mizuki content repository configuration
# Auto-generated by the init script

CONTENT_REPO_URL=${repoUrl.trim()}
CONTENT_DIR=./content
`;

	fs.writeFileSync(envPath, envContent);
	console.log("Created .env file");

	// Sync content
	console.log("Syncing content repository...");
	try {
		exec("pnpm run sync-content", {
			cwd: rootDir,
			env: {
				...process.env,
				CONTENT_REPO_URL: repoUrl.trim(),
			},
		});
		console.log("Content sync succeeded");
	} catch (error) {
		console.error(
			"Content sync failed. Run manually: pnpm run sync-content",
		);
	}

	// Show next steps
	console.log("\nInitialization complete\n");
	console.log("\nRelated docs:");
	console.log("- Content repository: docs/CONTENT_REPOSITORY.md");
	console.log("- Migration guide: docs/MIGRATION_GUIDE.md");

	rl.close();
}

main().catch((error) => {
	console.error("Initialization failed:", error);
	rl.close();
	process.exit(1);
});
